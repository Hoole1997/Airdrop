    fun getRandomHardwareIdentifier(): String {
        val randomCpuArchitecture = if (Random.nextBoolean()) "x64" else "x86"
        val randomCpuModel = "Fake CPU Model ${Random.nextInt(1000)}"
        val randomNumOfProcessors = Random.nextInt(1, 9)
        val randomTotalMemory = Random.nextLong(1024L * 1024 * 1024, 17L * 1024 * 1024 * 1024)

        val cpuInfo = mapOf(
            "cpuArchitecture" to randomCpuArchitecture,
            "cpuModel" to randomCpuModel,
            "numOfProcessors" to randomNumOfProcessors,
            "totalMemory" to randomTotalMemory
        )

        return Base64.getEncoder().encodeToString(cpuInfo.toString().toByteArray())
    }

    fun generateDeviceIdentifier(): String {
        val hardwareIdentifier = getRandomHardwareIdentifier()
        val deviceInfo = """{"hardware":"$hardwareIdentifier"}"""
        val md = MessageDigest.getInstance("SHA-256")
        return md.digest(deviceInfo.toByteArray()).joinToString("") { "%02x".format(it) }
    }

